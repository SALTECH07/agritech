export type DictKey = keyof (typeof DICT)["sw"];

const LangCtx = createContext<{
  lang: Lang;
  setLang: (l: Lang) => void;
  t: (k: DictKey) => string;
}>({
  lang: "sw",
  setLang: () => {},
  t: (k) => DICT.sw[k],
});

export function LangProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Lang>("sw");

  useEffect(() => {
    const saved =
      typeof window !== "undefined" ? (localStorage.getItem("vk_lang") as Lang | null) : null;
    if (saved === "sw" || saved === "en") setLangState(saved);
  }, []);

  const setLang = (l: Lang) => {
    setLangState(l);
    if (typeof window !== "undefined") localStorage.setItem("vk_lang", l);
  };

  const t = (k: DictKey) => DICT[lang][k] ?? DICT.sw[k] ?? k;

  return <LangCtx.Provider value={{ lang, setLang, t }}>{children}</LangCtx.Provider>;
}

export function useT() {
  return useContext(LangCtx);
}
